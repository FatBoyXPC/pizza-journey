    <p class="mb-6">
        In all honesty, Varasano's pizza is so good that I could believe you
        teleported me a pizza right from Napoli. Don't believe me? The image at
        the top of this page is from L'Antica Pizzeria da Michele. The image
        next to this paragraph is from Varasano's. Eating this pizza took me
        right back to my trip to Napoli. This is a bit unfair of a comparison
        as Jeff normally cooks his pizza just a little bit differently, but
        this is proof that he is more than capable of bringing a piece of
        Napoli over to the US.
    </p>

    <p class="mb-6">
        Varasano's has left me craving more, but I can't seem to find the same
        quality of pizza in Tampa. The saying goes that if you want the job
        done right to do it yourself. I've taken that attitude with BBQ, and
        had always planned to eventually start making my own pizza, but I
        wasn't prioritizing it. Since a good friend of mine decided to give me
        such a gift (knowing how snobby about pizza I am), I'm going to finally
        learn how to make such a prized pizza. My goal is to make a pizza that
        sends me back to Napoli, just like Jeff's pizza did. I hope you enjoy
        watching my journey!
    </p>
