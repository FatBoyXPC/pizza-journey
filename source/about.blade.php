    <h2>Why Pizza Making?</h2>

    <p class="mb-6">
        If you had asked me a few years ago what a blog of this title would be
        about, it likely would have been about finally finding a good pizza.
        I'm from the midwest, which isn't really known for pizza, except for the
        weird reason that Chicago's pizza casserole (inside out deep dish) has
        quite the notoriety.
    </p>

    <p class="mb-6">
        I was constantly unhappy with all of the pizza I had. I ended up in New
        York City back in 2014 and even the 3 random pizza places I tried then
        didn't impress me. I intentionally avoided looking anything up online
        so that I could avoid overhyped and well marketed places, and trusted
        some locals to guide me. Turns out maybe I should have done both, as I
        still hadn't found anything I like.
    </p>

    <p class="mb-6">
        In 2015 I ended up in Atlanta, and in a random set of events, at
        <a href="http://www.varasanos.com/" target="_blank">Varasano's Pizzeria</a>. Going in I wasn't
        expecting to be that impressed, as I had always been a bit disappointed
        with pizza. I saw on the menu that this was a thin crust pizza, and I
        feared a crunchy, crumbly, cracker like crust. I couldn't have been
        more wrong. This pizza was soft, not crunchy. It was light. The crust
        was springy and chewy (in a good way). It was everything opposite of
        what I previously thought of as a "thin crust pizza", yet, the crust
        was still somewhat thin. It's hard to explain, but to say the least, I
        absolutely fell in love with this pizza. The owner, Jeff, is also very
        helpful. He has quite a few videos on his
        <a href="https://www.youtube.com/user/varasano/videos" target="_blank">YouTube Channel</a>
        showing you how to stretch dough, bake the pizzas, etc. He even has
        <a href="http://www.varasanos.com/PizzaRecipe.htm" target="_blank">his recipe</a>
        and experimentation results online.
    </p>

    <p class="mb-6">
        This is when I learned what Neapolitan pizza (or pizza napoletana, if
        you want to sound cultured) was. I had no idea this was its own style
        of pizza. After speaking with Jeff, though, I learned there was an
        entire world of pizzas like this out there, I just didn't know what to
        look for.
    </p>

    <p class="mb-6">
        I started focusing on this type of pizza and it absolutely changed how
        much I enjoyed pizza. I was finally happy with (most of) the pizza
        places I found. In June 2019 I finally made my way to Napoli, or
        Naples, Italy. I can honestly say that clearly my preferred style of
        pizza is this, and that it's also difficult (though not impossible) to
        find a "bad" pizza in Napoli.
    </p>

    <img src="{{ $page->baseUrl }}/assets/img/varasanos.png"
        alt="Varasano's Pizzeria"
